//-------------------------------------------------------------------------------
// Imports
//-------------------------------------------------------------------------------

import {
    Class
} from 'bugcore';
import { Entity } from './';


//-------------------------------------------------------------------------------
// Declare Class
//-------------------------------------------------------------------------------

/**
 * @class
 * @extends {Entity}
 */
const UserEntity = Class.extend(Entity, {

    _name: 'recipe.UserEntity',


    //-------------------------------------------------------------------------------
    // Getters and Setters
    //-------------------------------------------------------------------------------

    /**
     * @return {string}
     */
    getEmail() {
        return this.rawData.email;
    },

    /**
     * @return {string}
     */
    getId() {
        return this.rawData.id;
    },

    /**
     * @return {string}
     */
    getUsername() {
        return this.rawData.username;
    }
});


//-------------------------------------------------------------------------------
// Exports
//-------------------------------------------------------------------------------

export default UserEntity;
