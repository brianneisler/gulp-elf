//-------------------------------------------------------------------------------
// Imports
//-------------------------------------------------------------------------------

import {
    Class,
    IObjectable,
    Obj
} from 'bugcore';
import { Data } from './';


//-------------------------------------------------------------------------------
// Declare Class
//-------------------------------------------------------------------------------

/**
 * @class
 * @extends {Data}
 */
const QueryResultData = Class.extend(Data, {

    _name: 'recipe.QueryResultData',


    //-------------------------------------------------------------------------------
    // Getters and Setters
    //-------------------------------------------------------------------------------

    /**
     * @return {string}
     */
    getName() {
        return this.getRawData().name;
    },

    /**
     * @return {string}
     */
    getVersionNumber() {
        return this.getRawData().versionNumber;
    },


    //-------------------------------------------------------------------------------
    // Data Methods
    //-------------------------------------------------------------------------------

    /**
     * @return {string}
     */
    toCacheKey() {
        return this.name + '-' + this.versionNumber;
    }
});


//-------------------------------------------------------------------------------
// Exports
//-------------------------------------------------------------------------------

export default QueryResultData;
